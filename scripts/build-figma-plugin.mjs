import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const manifest = JSON.parse(await readFile(resolve(root, "manifest.json"), "utf8"));

const icons = await Promise.all(
  manifest.icons.map(async ({ name, file }) => ({
    name,
    svg: await readFile(resolve(root, "icons", file), "utf8"),
  })),
);

const code = `// Generated by scripts/build-figma-plugin.mjs — do not edit.
const ICONS = ${JSON.stringify(icons)};
const PAGE_NAME = "Publr Icons";
const COLUMNS = 6;
const CARD_WIDTH = 160;
const CARD_HEIGHT = 120;
const GAP = 16;
const PAGE_PADDING = 40;
const GRID_TOP = 132;
const FONT = { family: "Inter", style: "Regular" };
const ROWS = Math.ceil(ICONS.length / COLUMNS);
const LIBRARY_WIDTH = PAGE_PADDING * 2 + COLUMNS * CARD_WIDTH + (COLUMNS - 1) * GAP;
const LIBRARY_HEIGHT = GRID_TOP + ROWS * CARD_HEIGHT + (ROWS - 1) * GAP + PAGE_PADDING;

const solid = (r, g, b) => ({ type: "SOLID", color: { r, g, b } });

function createText(characters, size, color) {
  const text = figma.createText();
  text.fontName = FONT;
  text.fontSize = size;
  text.characters = characters;
  text.fills = [solid(color.r, color.g, color.b)];
  return text;
}

// Figma's SVG importer can ignore the root fill="none" and fill stroked
// paths by default. Make the intended outline treatment explicit per element.
function normalizeSvg(svg) {
  return svg.replace(/<(path|circle|rect|line|polyline|polygon)(\\s[^>]*stroke="[^"]+"[^>]*)\\/>/g, (match, tag, attrs) => {
    if (/\\bfill=/.test(attrs)) return match;
    return "<" + tag + " fill=\\\"none\\\"" + attrs + "/>";
  });
}

function clearImplicitFills(node) {
  if (node.type === "VECTOR" && node.strokes && node.strokes.length > 0) {
    node.fills = [];
  }
  if ("children" in node) {
    for (const child of node.children) clearImplicitFills(child);
  }
}

async function generate() {
  await figma.loadFontAsync(FONT);

  let page = figma.currentPage;
  if (page.children.length === 0) {
    page.name = PAGE_NAME;
  } else {
    page = figma.createPage();
    page.name = PAGE_NAME;
    await figma.setCurrentPageAsync(page);
  }

  const library = figma.createFrame();
  library.name = "Publr Icons Library";
  library.resize(LIBRARY_WIDTH, LIBRARY_HEIGHT);
  library.x = 0;
  library.y = 0;
  library.fills = [solid(0.97, 0.975, 0.98)];
  library.clipsContent = false;
  library.setPluginData("generator", "publr-icons");

  const title = createText(PAGE_NAME, 32, { r: 0.09, g: 0.09, b: 0.1 });
  title.name = "Title";
  title.x = PAGE_PADDING;
  title.y = 40;
  library.appendChild(title);

  const subtitle = createText(
    ICONS.length + " editable 24×24 UI icon components · generated from publr-icons",
    14,
    { r: 0.42, g: 0.44, b: 0.47 },
  );
  subtitle.name = "Description";
  subtitle.x = PAGE_PADDING;
  subtitle.y = 84;
  library.appendChild(subtitle);

  const cards = [];
  const components = [];

  for (let index = 0; index < ICONS.length; index++) {
    const icon = ICONS[index];
    const column = index % COLUMNS;
    const row = Math.floor(index / COLUMNS);

    const card = figma.createFrame();
    card.name = "Preview/" + icon.name;
    card.resize(CARD_WIDTH, CARD_HEIGHT);
    card.x = PAGE_PADDING + column * (CARD_WIDTH + GAP);
    card.y = GRID_TOP + row * (CARD_HEIGHT + GAP);
    card.cornerRadius = 10;
    card.fills = [solid(1, 1, 1)];
    card.strokes = [solid(0.87, 0.88, 0.89)];
    card.strokeWeight = 1;
    card.clipsContent = false;
    library.appendChild(card);

    const imported = figma.createNodeFromSvg(normalizeSvg(icon.svg));
    if (!/fill="currentColor"/.test(icon.svg)) clearImplicitFills(imported);
    const component = figma.createComponentFromNode(imported);
    component.name = "Icon/" + icon.name;
    component.description = "Publr UI icon: " + icon.name + ". Canonical 24×24 SVG from publr-icons.";
    component.setPluginData("name", icon.name);
    component.exportSettings = [{ format: "SVG", suffix: "" }];
    card.appendChild(component);
    component.x = (CARD_WIDTH - component.width) / 2;
    component.y = 28;

    const label = createText(icon.name, 12, { r: 0.12, g: 0.12, b: 0.13 });
    label.name = "Name";
    card.appendChild(label);
    label.x = (CARD_WIDTH - label.width) / 2;
    label.y = 78;

    cards.push(card);
    components.push(component);
  }

  figma.currentPage.selection = [];
  figma.viewport.scrollAndZoomIntoView(cards.slice(0, COLUMNS * 2));
  figma.notify("Created " + ICONS.length + " Publr icon components on the “" + page.name + "” page.");
  figma.closePlugin();
}

generate().catch((error) => {
  console.error(error);
  figma.closePlugin("Could not generate Publr icons: " + (error && error.message ? error.message : error));
});
`;

await writeFile(resolve(root, "figma-plugin/code.js"), code);
console.log(`Generated Figma plugin for ${icons.length} UI icons.`);
